package example1;

public class Casting {

	public static void main(String[] args) 
	{
		
		int a=10; //"a" is a variable of "int" type
		float  b=(float)a; //variable(a) of "int" type will be changed to "float" type
		System.out.println(b);
		
		 
		float c=4.15f; //"c" is a variable of "float" type
		 
	    int d =(int)c; //variable(c) of "float" type will be changed to "int" type
		
		System.out.println(d);
		
		
		
		
		
		
		
		 

	}

}
