package collection;

class Type1 
{

	public static void main(String[] args) 
	{
		int a=10; //Variable of "int" type
		
	   float  b=(float)a; //10.0 converted of "a" type(int) into type(float) 
	   
	   System.out.println(b);
	   
	   
	   float c=20.5f; //variable of "float" type
	   
	   int d=(int)c; //20 is converted of "c" type(float) into type(int)
	   
	   System.out.println(d);
	   
	}
}
		
		             